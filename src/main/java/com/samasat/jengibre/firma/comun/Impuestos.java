@XmlAccessorType (XmlAccessType.FIELD)

public class Impuestos {
    private List<Impuesto> impuestos;

    public List<Impuesto> getImpuestos() {
        return impuestos;
    }

    public void setImpuestos(List<Impuesto> impuestos) {
        this.impuestos = impuestos;
    }
    
}
